    private void BTLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTLogarActionPerformed
        String email = JTFemail.getText();
        String senha = PFsenha.getText();

        List<AzureUser> consultaCadastro = ConexaoAzure.jdbcTemplate.query(
                "Select * from tbUsuario where emailUsuario = ? and senha = ?",
                new BeanPropertyRowMapper(AzureUser.class),
                email, senha);

        if (!consultaCadastro.isEmpty()) {
            for (AzureUser users : consultaCadastro) {
                JOptionPane.showMessageDialog(null, "Login efetuado com sucesso. Bem vindo(a) "
                        + users.getNomeUsuario());

                try {
                    enviarMensagemLoginSlack(users.getNomeUsuario());
                } catch (IOException | InterruptedException e) {
                    System.out.println(e);
                }

                new TelaDash().setVisible(true);
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Email ou senha incorreto!");
        }
    }//GEN-LAST:event_BTLogarActionPerformed

